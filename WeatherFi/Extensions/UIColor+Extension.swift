import UIKit

// MARK: UIColor Extension
extension UIColor {
    class var accentColor: UIColor {
        return UIColor(red: 0.043, green: 0.047, blue: 0.118, alpha: 1.0)
    }
    
    class var secondaryAccentColor: UIColor {
        return UIColor(red: 0.078, green: 0.098, blue: 0.196, alpha: 1.0)
    }
}
